package Parsing.Creators;

public class CustomObject {

  public String starID;
  public String properName;
  public String x;
  public String y;
  public String z;

  public CustomObject(String starID, String properName, String x, String y, String z) {
    this.starID = starID;
    this.properName = properName;
    this.x = x;
    this.y = y;
    this.z = z;
  }
}
